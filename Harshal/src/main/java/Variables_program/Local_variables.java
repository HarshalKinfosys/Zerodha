package Variables_program;

public class Local_variables {// class body is open
	public static void main(String[] args) {// main method body is open
		int i=10;
		System.out.println(i);// i=10
		
		long donky=1234567890l;
		System.out.println(donky);
		
		char ch='H';
		System.out.println(ch);
		
		boolean b=true;
		System.out.println(b);
		
		String s="Velocity";
        System.out.println(s);
		
		
	}// main method is closed
	
}// class is closed
